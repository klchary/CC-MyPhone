package com.example.ccmyphone.Models;

public class EduQualifications {

    private int layoutId;
    private String qualificationTitle;
    private String institutionName;
    private String institutionDist;
    private String institutionState;
    private String courseName;
    private String coureFrom;
    private String courseTo;
    private int percentage;

    public int getLayoutId() {
        return layoutId;
    }

    public void setLayoutId(int layoutId) {
        this.layoutId = layoutId;
    }

    public String getQualificationTitle() {
        return qualificationTitle;
    }

    public void setQualificationTitle(String qualificationTitle) {
        this.qualificationTitle = qualificationTitle;
    }

    public String getInstitutionName() {
        return institutionName;
    }

    public void setInstitutionName(String institutionName) {
        this.institutionName = institutionName;
    }

    public String getInstitutionDist() {
        return institutionDist;
    }

    public void setInstitutionDist(String institutionDist) {
        this.institutionDist = institutionDist;
    }

    public String getInstitutionState() {
        return institutionState;
    }

    public void setInstitutionState(String institutionState) {
        this.institutionState = institutionState;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getCoureFrom() {
        return coureFrom;
    }

    public void setCoureFrom(String coureFrom) {
        this.coureFrom = coureFrom;
    }

    public String getCourseTo() {
        return courseTo;
    }

    public void setCourseTo(String courseTo) {
        this.courseTo = courseTo;
    }

    public int getPercentage() {
        return percentage;
    }

    public void setPercentage(int percentage) {
        this.percentage = percentage;
    }
}
